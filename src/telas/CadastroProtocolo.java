    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        BancoDados bd = new BancoDados();
        try {
            bd.setConnection();
            String num = bd.cadastraProtocolo(txtCliente.getText(), txtNomeContato.getText(), txtTelefone.getText(), txtEmail.getText(), filtro1.getSelectedItem().toString(), filtro2.getSelectedItem().toString(), filtro3.getSelectedItem().toString(), filtro4.getSelectedItem().toString(), situacao.getSelectedItem().toString(), tratamento.getSelectedItem().toString(), txtMemorando.getText());
            JOptionPane.showMessageDialog(this, "Protocolo "+num+" gerado com sucesso!");
            dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Por favor, verifique os dados a serem inseridos e tente novamente!");
            Logger.getLogger(CadastroProtocolo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                        

