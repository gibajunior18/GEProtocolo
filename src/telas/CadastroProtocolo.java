    private void filtro1ActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        BancoDados bd = new BancoDados();
        try {
            ArrayList<String> list = (ArrayList<String>) bd.listaFiltro("cd_filtro2", "cd_filtro1", filtro1.getSelectedItem().toString());
            filtro2.removeAllItems();
            DefaultComboBoxModel combo = new DefaultComboBoxModel(list.toArray());
            filtro2.setModel(combo);
            filtro2.updateUI();
        } catch (SQLException ex) {
            Logger.getLogger(CadastroProtocolo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                       

